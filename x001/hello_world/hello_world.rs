// In rust in order to create function you define the function with `fn` and the name. Every rust program requires a main function as an entry point just like in C

fn main() {
    // Like python uses 4 line space indentation vs tab
    println!("Hello, world from Rust!");
    // That `!` after `println` means it s a macro!
    // `;` marks the end of the expression.
}

